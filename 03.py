TREE = "#"
LINE_LEN = 31
lines = map.split('\n')


def count_trees(slope):
    i = 0
    trees = 0
    right, down = slope
    for index in range(down, len(lines), down):
        i = i + right
        target = lines[index][i % LINE_LEN]
        if target == TREE:
            trees = trees + 1
    return trees


# (right, down)
PART_ONE_SLOPE = (3, 1)

print("PART1: ", count_trees(PART_ONE_SLOPE))

SLOPES = [
    (1, 1),
    (3, 1),
    (5, 1),
    (7, 1),
    (1, 2),
]

total = []
for slope in SLOPES:
    total.append(count_trees(slope))

print("PART2: ", prod(total))
